    @Override
    public void paint(Graphics g){
        super.paint(g);
        
        int total = afirmacion + negacion;
        
        int grados_afirmacion = afirmacion * 360 / total;
        int grados_negacion = negacion * 360 / total;
        
        g.setColor(new Color(204,006,005));
        g.fillArc(25, 100, 270, 270, 0, grados_afirmacion);
        g.fillRect(310,120,20,20);
        g.drawString(afirmacion + " pagaron la inscripcion", 340, 135);
        
        g.setColor(new Color(87,166,057));
        g.fillArc(25, 100, 270, 270, grados_afirmacion, grados_negacion);
        g.fillRect(310,150,20,20);
        g.drawString(negacion + " no pagaron", 340, 165);
        
    }
    
