    public Grafica() {
        initComponents();

        user = Login.user;

        setTitle("Estadistica");
        setSize(550, 450);
        setResizable(false);
        setLocationRelativeTo(null);

        setDefaultCloseOperation(WindowConstants.DISPOSE_ON_CLOSE);

        ImageIcon wallpaper = new ImageIcon("src/images/fondo5.jpg");
        Icon icono = new ImageIcon(wallpaper.getImage().getScaledInstance(jLabel_Wallpaper.getWidth(),
                jLabel_Wallpaper.getHeight(), Image.SCALE_DEFAULT));

        jLabel_Wallpaper.setIcon(icono);
        this.repaint();

        try {

            Connection cn = Conexion.conectar();
            PreparedStatement pst = cn.prepareStatement(
                    "select inscripcion, count(inscripcion) as Inscripcion from alumnos group by inscripcion");
            ResultSet rs = pst.executeQuery();

            if (rs.next()) {
                int posicion = 0;
                do {
                    vector_inscripcion[posicion] = rs.getString(1);
                    vector_cantidad[posicion] = rs.getInt(2);                    
                    

                    if (vector_inscripcion[posicion].equalsIgnoreCase("SI")) {
                        afirmacion = vector_cantidad[posicion];
                    } else if (vector_inscripcion[posicion].equalsIgnoreCase("NO")) {
                        negacion = vector_cantidad[posicion];
                    }
                    posicion++;
                } while (rs.next());
            }

        } catch (SQLException e) {
            System.err.println("Error en la consulta. " + e);
            JOptionPane.showMessageDialog(null, "ERROR en consultar datos, contacte al administrador.");
        }

        repaint();
    }

